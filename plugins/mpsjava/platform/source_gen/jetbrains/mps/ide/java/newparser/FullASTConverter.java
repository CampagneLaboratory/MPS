  private String typeName(TypeReference typeRef) {
    char[][] tokens = typeRef.getTypeName();
    if (tokens == null || tokens.length == 0) {
      return null;
    }

    StringBuffer sb = new StringBuffer();
    for (int i = 0; i < tokens.length; i++) {
      if (i > 0) {
        sb.append('.');
      }
      sb.append(tokens[i]);
    }
    return sb.toString();
  }

